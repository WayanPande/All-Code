    private void cariMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_cariMouseClicked
        String kunci = Tcari.getText();
        Tabel.setModel(new DefaultTableModel(null, new String[]{
                "ID", "No. Ktp", "No. Hp", "No. Kamar", "Nama 1", "Nama 2", "Jenis Kamar", "Tgl Checkin", "Tgl Checkout"
            }));
        TableColumnModel columnModel = Tabel.getColumnModel();
        columnModel.getColumn(0).setPreferredWidth(2);
        try{
            KonekMYSQL konek = new KonekMYSQL();
            java.sql.Connection c = konek.getKoneksi(); 
            Statement s = c.createStatement();
            String sql = "SELECT * FROM pelanggan WHERE nama_satu LIKE " + "'%" + kunci + "%' OR no_ktp LIKE "  + "'%" + kunci + "%' OR no_hp LIKE "  + "'%" + kunci + "%' OR no_kamar LIKE" + "'%" + kunci + "%' OR nama_dua LIKE " + "'%" + kunci + "%' OR jenis_kamar LIKE "+ "'%" + kunci + "%'";
            ResultSet r = s.executeQuery(sql);
            while(r.next()){
              
              String data[] = {r.getString("id"), r.getString("no_ktp"), r.getString("no_hp"), r.getString("no_kamar"), r.getString("nama_satu"), r.getString("nama_dua"), r.getString("jenis_kamar"), r.getString("tgl_checkin"), r.getString("tgl_checkout")};
       
              DefaultTableModel tblModel = (DefaultTableModel)Tabel.getModel();
              tblModel.addRow(data);
            }
            r.close();
            s.close();
        }catch(SQLException e){
            System.out.println("terjadi error Search data");
        }
    }//GEN-LAST:event_cariMouseClicked

