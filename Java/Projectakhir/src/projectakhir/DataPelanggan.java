    private void TubahMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TubahMouseClicked
        int row = Tabel.getSelectedRow();
        String ktp = Tktp.getText();
        String noHp = Thp.getText();
        String Nokamar = Tkamar.getText();
        String namasatu = Tnamasatu.getText();
        String namadua = Tnamadua.getText();
        String jenisKamar = Tjeniskamar.getText();
        String id = Tabel.getModel().getValueAt(row, 0).toString();
        
        int a=JOptionPane.showConfirmDialog(this,"Anda yakin?");  
        
        if (a==JOptionPane.YES_OPTION){
            try{
               KonekMYSQL konek = new KonekMYSQL();
               java.sql.Connection c = konek.getKoneksi(); 
               Statement s = c.createStatement();
               String sql = "UPDATE pelanggan SET no_ktp='%s', no_hp='%s', no_kamar='%s', nama_satu='%s', nama_dua='%s', jenis_kamar='%s' WHERE id='" + id + "'";
               sql = String.format(sql, ktp, noHp, Nokamar, namasatu, namadua, jenisKamar);
               s.execute(sql);
           }catch(SQLException e){
               System.out.println("terjadi error update data");
               System.out.println(e.getMessage());
           }
           Tabel.setModel(new DefaultTableModel(null, new String[]{
                    "ID", "No. Ktp", "No. Hp", "No. Kamar", "Nama 1", "Nama 2", "Jenis Kamar", "Tgl Checkin", "Tgl Checkout"
               }));

           loadData();
           TableColumnModel columnModel = Tabel.getColumnModel();
           columnModel.getColumn(0).setPreferredWidth(2);
           JOptionPane.showMessageDialog(this, "Data Berhasil Diubah");
           Tktp.setText("");
           Thp.setText("");
           Tkamar.setText("");
           Tnamasatu.setText("");
           Tnamadua.setText("");
           Tjeniskamar.setText("");
        }
    }//GEN-LAST:event_TubahMouseClicked
